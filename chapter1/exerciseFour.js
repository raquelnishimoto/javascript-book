/*
** An expression is anything that JavaScript can evaluate to a value,
** even if that value is undefined or null. With only a few exceptions,
** almost everything you write in JavaScript is an expression. 
** JavaScript expressions always evaluate to a value.
** Statements always evaluate as undefined. They differ from expressions 
** in that you cannot use a statement as part of another expression. 
*/

// statement
var foo;

// expression
foo = 5;

// expression
foo;