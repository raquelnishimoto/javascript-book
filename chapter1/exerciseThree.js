/*
Identify the data type for each of the following values:
*/

// 'true'
const dataOne = 'true';
console.log(`${dataOne} datatype is ${typeof dataOne}`);

// false
const dataTwo = false;
console.log(`${dataTwo} datatype is ${typeof dataTwo}`);

// 1.5
const dataThree = 1.5;
console.log(`${dataThree} datatype is ${typeof dataThree}`);

// 2
const dataFour = 2;
console.log(`${dataFour} datatype is ${typeof dataFour}`);

// undefined
const dataFive = undefined;
console.log(`${dataFive} datatype is ${typeof dataFive}`);

// { foo: 'bar' }
const dataSix = { foo: 'bar' };
console.log(`${dataSix} datatype is ${typeof dataSix}`);